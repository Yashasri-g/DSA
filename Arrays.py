# Declaration of arrays
# Using a list to represent an array in Python
arr = []
print(arr)
print(type(arr))
# Using numpy to create an array
import numpy as np
arr_numpy = np.array([])
print(arr_numpy)
print(type(arr_numpy))
# Using a list to create an array with initial values
arr_with_values = [1, 2, 3, 4, 5]
print(arr_with_values)
print(type(arr_with_values))
# Using numpy to create an array with initial values
arr_numpy_with_values = np.array([1, 2, 3, 4, 5])
print(arr_numpy_with_values)
print(type(arr_numpy_with_values))